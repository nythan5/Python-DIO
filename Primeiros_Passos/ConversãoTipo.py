'''Conversão de Inteiro para Float'''

preco = 10
print (preco,"numero inteiro")

preco = float(preco)
print(preco,"numero float")


'''Conversão de Float para Inteiro'''

preco = 10.30
print (preco,"numero float")

preco = int(preco)
print(preco,"numero inteiro")


'''Conversão por divisao'''

preco = 10
print(preco)

print(preco/2,"numero float")
print(preco//2,"numero inteiro")

'''Conversão numérico para string'''

preco = 10.50
idade = 28

print(str(preco))
print(str(idade))

texto = f"idade {idade} preco {preco}"
print(texto)

''' Conversão de String para Número'''

preco = "10.53"
idade = "28"

